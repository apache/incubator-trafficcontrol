package com.comcast.cdn.traffic_control.models;

public class Alert {
	private String level;
	private String text;
	
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
